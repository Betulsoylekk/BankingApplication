package accounttype;

/**
 * Enumaration class for all account types in the system
 */
public enum AccountType {
	FCW, // foreign currency account with interest
	FCWO, // foreign currency account without interest
	GW,   // gold account with interest
	GWO, // gold account without interest
	I,   // investment account with interest
	RW,  // regular account with interest
	RWO;  // regular account without interest

}
